#WHILE
#Las estructuras de repetición son las llamadas estructuras cíclicas, iterativas
#o de bucles. Permiten ejecutar un conjunto de instrucciones de manera
#repetida(o cíclica) mientras que la expresión lógica a evaluar se cumpla(sea
#verdadera)
#Quiero imprimir 1..100 numeros
c=1#contador
while c<=100:
    print(c)
    c=c+1
